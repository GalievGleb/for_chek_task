Fri = int(input("Введите количество тест-кейсов, написанных в пятницу: ").strip())
Sat = int(input("Введите количество тест-кейсов, написанных в субботу: ").strip())
Sun = int(input("Введите количество тест-кейсов, написанных в воскресенье: ").strip())
