Mon = int(input("Введите количество тест-кейсов, написанных в понедельник: ").strip())
Tue = int(input("Введите количество тест-кейсов, написанных во вторник: ").strip())
Wen = int(input("Введите количество тест-кейсов, написанных в среду: ").strip())
